public class TipoDeVariaveis {

    /*
    Estrutura padrão a ser seguida:
    
    <Tipo> <nomeVariavel> <atribuiçãoDeValor(Opcional)>

    EX: int idade; <- Tipo "int", nome "idade" , sem nenhum valor atribuido
        int ano = 2024 <- Tipo int, nome "ano", Valor 2024 
        double reais = 2.5
        String nome = "Julio"
    ----------------------------------------------------------------------------

    Já as constantes são valores que não podem ser modificados. São representados 
    pela palavra reservada "final". Pòr convenção,as constantes são escritas em 
    CAIXA ALTA
    
    EX: final double VALOR_DE_PI = 3.14;
    final String NOME = "Julio"
    */
    
}